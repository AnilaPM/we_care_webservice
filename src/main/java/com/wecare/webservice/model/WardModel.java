package com.wecare.webservice.model;

public class WardModel {
	
	int ward_id;
	int ward_no;
	int ward_population;
	int ward_houses;
	
	
	public int getWard_id() {
		return ward_id;
	}
	public void setWard_id(int ward_id) {
		this.ward_id = ward_id;
	}
	public int getWard_no() {
		return ward_no;
	}
	public void setWard_no(int ward_no) {
		this.ward_no = ward_no;
	}
	public int getWard_population() {
		return ward_population;
	}
	public void setWard_population(int ward_population) {
		this.ward_population = ward_population;
	}
	public int getWard_houses() {
		return ward_houses;
	}
	public void setWard_houses(int ward_houses) {
		this.ward_houses = ward_houses;
	}

}
