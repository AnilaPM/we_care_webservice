package com.wecare.webservice.model;

public class RouteMapModel {
	int ward_no;
	String place_name;
	String wp_name;
	String route_date;
	String route_time;
	public int getWard_no() {
		return ward_no;
	}
	public void setWard_no(int ward_no) {
		this.ward_no = ward_no;
	}
	public String getPlace_name() {
		return place_name;
	}
	public void setPlace_name(String place_name) {
		this.place_name = place_name;
	}
	public String getWp_name() {
		return wp_name;
	}
	public void setWp_name(String wp_name) {
		this.wp_name = wp_name;
	}
	public String getRoute_date() {
		return route_date;
	}
	public void setRoute_date(String route_date) {
		this.route_date = route_date;
	}
	public String getRoute_time() {
		return route_time;
	}
	public void setRoute_time(String route_time) {
		this.route_time = route_time;
	}

}
